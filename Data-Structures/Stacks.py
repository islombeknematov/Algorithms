# STACKS

                        # 1.1 Theory

# A stack is a LIFO ( Last In First Out). This structure is named
# as "stack" because it resembles a real-world stack - a stack of plates

# https://miro.medium.com/max/630/1*e4XWkyzxdOreblbPlbOCyw.jpeg


                        # 1.2 Stack Operations

# 1) PUSH - Insert an element on the top of the stack
# 2) POP - Delete the topmost element and return it
# https://miro.medium.com/max/630/1*QMifqahZm4DGQ91GkOhu4g.png


                        # 1.3 Additional functions to check Stack's status

# 1) Peek - Return the top element of the stack without deleting it.
# 2) isEmpty - Check if the stack is empty
# 2) isFull - Check if the stack is full
